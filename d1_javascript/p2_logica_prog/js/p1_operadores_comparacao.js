/*
***operadores de atribuicao

** maior que - >
** maior que ou igual a - >=
** menor que - <
** menor que ou igual a - <=
** igualdade - == (valor)
** igualdade estrita - === (valor e tipo)
** difirente - != (valor)
** difirente - !== (valor e tipo)
*/

const valor = 10 > 5;
const valor2 = 5 >= 4;
console.log(valor) // o mesmo vale para <
console.log(valor2) // o mesmo vale para <=

const num1 = 10;
const num2 = '10';
const comp = num1 === num2;
const comp2 = num1 == num2;
console.log(comp);
console.log(comp2);
