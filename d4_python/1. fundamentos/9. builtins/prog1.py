# fundamentos
'''
    dir
    - dir ([objeto]) -> lista de strings
    - retorne uma lista em ordem alfabética de nomes compreendendo (alguns dos) atributos
    do objeto dado e dos atributos alcançáveis a partir dele.

    __builtins__
    
    - guarda algumas das funções que podem ser usadas no python, que estão disponiveis por padrão

    len
    - informa o tamanho de uma string
'''
type(1)
__builtins__.type('diogo')
__builtins__.print(10/3)
print(dir(__builtins__))

help(__builtins__)

nome = 'diogo dias mello'
print(type(nome))
__builtins__.len(nome)

print(dir)
